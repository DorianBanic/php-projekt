            <section class="content">
                <p>UTAKMICA između Bjelovara i Dinama u osmini finala SuperSport Hrvatskog nogometnog kupa odgođena je zbog loših vremenskih uvjeta, potvrdio je hrvatski prvak. "Zbog loših vremenskih uvjeta odlučeno je da se današnja utakmica osmine finala Kupa neće odigrati. Klub će obavijestiti javnost o novom terminu utakmice čim on bude poznat", objavili su iz Dinama na društvenim mrežama.</p>
                <p>Jedna od tih osam utakmica sad je odgođena, a novi termin njenog odigravanja još se čeka. Utakmica je odgođena tek nešto više od sat vremena prije početka zakazanog u 13:30. Dinamo je termin za odigravanje ogleda u Bjelovaru bio prihvatio unatoč brojnim izostancima svojih najboljih igrača.</p>
                <p>"Ovo je bio jedini datum kad smo mogli igrati prije Nove godine i izašli smo ususret protivniku. Imamo širok kadar i vjerujemo našim igračima. U Bjelovaru ćemo suprotstaviti kvalitetnu momčad i, vjerujem, proći u iduće kolo Kupa", dodao je Bjelica kad je najavljivao utakmicu.</p>
                <p>Dinamu za tri dana slijedi ligaški ogled protiv Rijeke u kojem će biti domaćin, a potom sljedeće srijede utakmica protiv Borussije Dortmund u Ligi prvaka. Prvog prosinca igrat će najveći derbi hrvatskog nogometa, utakmicu s Hajdukom.</p>
                <p>Ranije su prolazak u četvrtfinale natjecanja izborili Osijek, Rijeka, Hajduk, Istra 1961, Gorica i Slaven Belupo, dok je dvoboj Dugopolja i Lokomotive zakazan za sljedeći tjedan.</p>
