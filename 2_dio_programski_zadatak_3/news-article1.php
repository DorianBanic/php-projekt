                <h1>Odgođena Dinamova kup-utakmica u Bjelovaru</h1>
                <h2>Utakmica nogometnog kupa odgođena je zbog loših vremenskih uvjeta</h2>
                <time datetime="2024-11-20">Objavljeno: 20. studenoga 2024.</time>
            </header>

            <figure>
                <img src="news/thumbnail1.jpg">
