            <section class="content">
                <p>HRVATSKI reprezentativac Mislav Oršić (31) izbačen je iz prve momčadi Trabzonspora. Stefano Denswil, Enis Bardhi i Umut Bozok također više nisu dio prve momčadi turskog prvoligaša, a u klubu smatraju da navedeni igrači moraju popraviti formu ako se misle vratiti u prvi tim.</p>
                <p>Vrlo je izvjesno da će Oršić već ove zime napustiti turski klub. Hrvatski ofenzivac ima osam nastupa ove sezone i 343 odigrane minute, a turski mediji već neko vrijeme pišu kako ga se klub planira riješiti. Kao jedna od najzanimljivijih opcija za Oršića nameće se Dinamo, klub njegovog života, što je u više navrata potvrdio.</p>
                <p>Modri bi se mogli aktivirati u prijelaznom roku oko ovog posla, tim više što su Oršić i trener Nenad Bjelica u prvom zajedničkom mandatu imali sjajnu suradnju. Uostalom, upravo je Bjelica doveo Oršića u Trabzonspor pretprošlog ljeta.</p>
                <p>Najbolje dane proveo je igrajući za Dinamo, s kojim je osvojio pet naslova prvaka Hrvatske te zabio 91 gol u 216 nastupa. Igrao je s Dinamom Ligu prvaka i Europa ligu, što mu je i donijelo poziv u reprezentaciju u rujnu 2019. godine.</p>
                <p>Oršić prema Transfermarktu vrijedi 2.2 milijuna eura, a ugovor s turskim klubom traje mu do idućeg ljeta. To značajno olakšava Dinamov put prema Oršiću jer bi ga Turci mogli pustiti ispod cijene.</p>
